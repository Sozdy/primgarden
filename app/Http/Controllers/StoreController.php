    public function update1c (Request $request) {
        foreach ($request->shops as $item) {
            $store = Store::whereId($item["id"])->first() ?? new Store();

            $store->id = $item["id"];
            $store->name = $item["name"];
            $store->price_type_id = $item["typePriceId"];
            $store->retail_price_type_id = $item["typeRetailPriceId"];
            $store->is_active = !$item["markDelete"];

            $store->save();
        }

        return response("success", 200);
    }

